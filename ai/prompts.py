def get_system_prompt(mode: str, custom_prompt: str = "") -> str:
    """获取指定模式的 System Prompt。

    优先使用用户自定义 prompt，若未设置则使用内置默认值。

    Args:
        mode: "B" / "C" / "D" / "E" / "F"
        custom_prompt: 用户在 config.toml 中配置的自定义 prompt（空字符串表示未配置）

    Returns:
        最终使用的 System Prompt 字符串

    Raises:
        ValueError: mode 不是 "B" / "C" / "D" / "E" / "F"
    """
    if mode not in ("B", "C", "D", "E", "F"):
        raise ValueError(f"get_system_prompt 不支持 mode={mode!r}，只支持 B / C / D / E / F")

    # 用户自定义优先
    if custom_prompt and custom_prompt.strip():
        return custom_prompt.strip()

    defaults = {
        "B": MODE_B_DEFAULT,
        "C": MODE_C_DEFAULT,
        "D": MODE_D_DEFAULT,
        "E": MODE_E_DEFAULT,
        "F": MODE_F_DEFAULT,
    }
    return defaults[mode]


def get_mode_description(mode: str) -> str:
    """获取模式的用户可读描述。

    Args:
        mode: "A" / "B" / "C" / "D" / "E" / "F"

    Returns:
        模式描述字符串，未知模式返回空字符串
    """
    meta = _MODE_METADATA.get(mode, {})
    return meta.get("description", "")


def get_mode_name(mode: str) -> str:
    """获取模式的显示名称。

    Args:
        mode: "A" / "B" / "C" / "D" / "E" / "F"

    Returns:
        模式名称字符串，未知模式返回模式 ID
    """
    meta = _MODE_METADATA.get(mode, {})
    return meta.get("name", mode)


def get_mode_icon(mode: str) -> str:
    """获取模式的图标 emoji。

    Args:
        mode: "A" / "B" / "C" / "D" / "E" / "F"

    Returns:
        图标字符串，未知模式返回空字符串
    """
    meta = _MODE_METADATA.get(mode, {})
    return meta.get("icon", "")


def list_supported_modes() -> List[str]:
    """返回所有支持的模式 ID 列表（有序）。

    Returns:
        ["A", "B", "C", "D", "E", "F"]
    """
    return list(_MODE_METADATA.keys())


def get_all_mode_metadata() -> Dict[str, Dict[str, str]]:
    """返回所有模式的元数据字典（只读副本）。

    Returns:
        {mode_id: {"name": ..., "description": ..., "icon": ...}, ...}
    """
    return {k: dict(v) for k, v in _MODE_METADATA.items()}
